export interface QuestSource {
  label: string;
  href: string;
}

export interface QuestExpansionSeed {
  slug: string;
  title: string;
  question: string;
  correct: string;
  wrong: string[];
  explanation: string;
  appLink: {
    href: string;
    label: string;
  };
  column?: string;
  sources?: QuestSource[];
}

function q(
  slug: string,
  title: string,
  question: string,
  correct: string,
  wrong1: string,
  wrong2: string,
  explanation: string,
  appLink: { href: string; label: string },
  column?: string,
  sources?: QuestSource[]
): QuestExpansionSeed {
  return {
    slug,
    title,
    question,
    correct,
    wrong: [wrong1, wrong2],
    explanation,
    appLink,
    column,
    sources
  };
}

